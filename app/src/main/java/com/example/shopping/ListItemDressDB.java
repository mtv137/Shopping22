package com.example.shopping;

public class ListItemDressDB {
    private String number;
    private String titleDress;
    private String hyperlinkDress;
    private String priceDress;

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
    public String getTitleDress() {
        return titleDress;
    }

    public void setTitleDress(String titleDress) {
        this.titleDress = titleDress;
    }

    public String getHyperlinkDress() {
        return hyperlinkDress;
    }

    public void setHyperlinkDress(String hyperlinkDress) {
        this.hyperlinkDress = hyperlinkDress;
    }

    public String getPriceDress() {
        return priceDress;
    }

    public void setPriceDress(String priceDress) {
        this.priceDress = priceDress;
    }
}
